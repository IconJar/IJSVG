- (NSString*)SVGString
{
    NSXMLNodeOptions options = NSXMLNodePrettyPrint;
    if (IJSVGExporterHasOption(_options, IJSVGExporterOptionCompressOutput) == YES) {
        options = NSXMLNodeOptionsNone;
